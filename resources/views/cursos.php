   <font size = "4" color = "white" >  <p align="justify"> HUVR es un dispositivo creado por ingenieros y especialistas de la Universidad de California. La máquina desarrolla una imagen que puede ser tocada por los usuarios como si se tratara de un objeto real, además de obtener estos resultados con un bajo costo de producción.

El dispositivo HUVR combina un panel en 3D de alta definición. </p> </font>

