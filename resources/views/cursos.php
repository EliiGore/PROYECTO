  <font size = "4" color = "white" >  <p align="justify"> Cyber Walk es otro de los últimos avances en RV. Es una plataforma en la que podemos movernos sin problemas en los mundos virtuales y, lo novedoso, es que sigue nuestros pasos, avancemos en la dirección que avancemos. No importa si nos movemos hacia adelante, atrás o en diagonal, ella nos seguirá según nuestros movimientos, permitiendo un avance constante y sin pausas (siempre y cuando no corramos). Esto podría solucionar uno de los principales problemas de la RV que era el de moverse por un mundo virtual ilimitado en un entorno real con limitaciones de espacio.

Este sistema revolucionario, creado por un consorcio de laboratorios alemanes, italianos y suizos, consiste en una plataforma omnidireccional que ajusta su velocidad y dirección a la del usuario para que éste permanezca siempre en el medio.</p> </font>
     
