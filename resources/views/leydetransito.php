<h4>DE LAS UNIDADES DEL CONTROL DEL TRANSPORTE TERRESTRE, TRÁNSITO Y SEGURIDAD VIAL DE LOS GOBIERNOS AUTÓNOMOS DESCENTRALIZADOS</h3>
El control del tránsito y la seguridad vial será ejercido por las autoridades regionales, metropolitanas o municipales en sus respectivas circunscripciones territoriales, a través de las Unidades de Control de Transporte Terrestre, Tránsito y Seguridad Vial de los Gobiernos Autónomos Descentralizados, constituidas dentro de su propia institucionalidad, unidades que dependerán operativa, orgánica, financiera y administrativamente de éstos. <br>Las Unidades de Control de Transporte Terrestre, Tránsito y Seguridad Vial de los Gobiernos Autónomos Descentralizados Regionales, Metropolitanos o Municipales, estarán conformadas por personal civil especializado, seleccionado y contratado por el Gobierno Autónomo Descentralizado y formado por la Agencia de Regulación y Control del Transporte Terrestre, Tránsito y Seguridad Vial. (Art. 30.2 Ley Orgánica de Transporte Terrestre Transito y Seguridad Vial Ecuador – Reformas 2011).</br>
Los Gobiernos Autónomos Descentralizados Regionales, Metropolitanos o Municipales son responsables de la planificación operativa del control del transporte terrestre, tránsito y seguridad vial, planificación que estará enmarcada en las disposiciones de carácter nacional emanadas desde la Agencia Nacional de Regulación y Control del Transporte Terrestre, Tránsito y Seguridad Vial, y deberán informar sobre las regulaciones locales que se legislen." <br></br>
(Art. 30.3 Ley Orgánica de Transporte Terrestre Transito y Seguridad Vial Ecuador Reformas 2011).
